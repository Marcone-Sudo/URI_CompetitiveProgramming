""" 

Basicamente temos ter estruturas condicionais no Python

if --> "Se" só pode ocorrer uma vez na estrutura condicional
elif --> "senão se" pode aparecer quantas vezes for necessário na estrutura condicional
else --> "senão" pode aparecer uma ou nenhuma vez em nossa estrutura condicional.

--> Operadores condicionais

AND --> Binário
OR  --> Binário
in  --> Unário
not --> Unário


"""

valor = 100

if valor == 200:
    print("OK")
elif valor == 150:
    print("ok")
elif valor == 100:
    print("BINGO")
elif valor == 50:
    print("OK")
else:
    print("Tente novamente")

